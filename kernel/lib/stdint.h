#ifndef STDINT_H
#define STDINT_H

// some typedefs to make things a little more sane
// written on X86 (32bit)

typedef unsigned int   u32int;
typedef          int   s32int;
typedef unsigned short u16int;
typedef          short s16int;
typedef unsigned char  u8int;
typedef          char  s8int;
typedef          int   bool;

#define true 1
#define false 0

#endif
